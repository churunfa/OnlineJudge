package OnlineJudge.domain;

public class ContestTime {
    private Long st;
    private Long ed;
    private Long now;
    private Long r;

    public Long getSt() {
        return st;
    }

    public void setSt(Long st) {
        this.st = st;
    }

    public Long getEd() {
        return ed;
    }

    public void setEd(Long ed) {
        this.ed = ed;
    }

    public Long getNow() {
        return now;
    }

    public void setNow(Long now) {
        this.now = now;
    }

    public Long getR() {
        return r;
    }

    public void setR(Long r) {
        this.r = r;
    }
}
